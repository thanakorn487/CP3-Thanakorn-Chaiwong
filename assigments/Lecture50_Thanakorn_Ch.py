def AddNumber(x,y):
    print(x, "+", y, "=", x + y)
def SubtractNumber(x,y):
    print(x, "-", y, "=", x - y)
def MultiplyNumber(x,y):
    print(x, "x", y, "=", x * y)
def DivideNumber(x,y):
    print(x, "/", y, "=", x / y)
a = int(input("Input x : "))
b = int(input("Input y : "))
AddNumber(a,b)
SubtractNumber(a,b)
MultiplyNumber(a,b)
DivideNumber(a,b)